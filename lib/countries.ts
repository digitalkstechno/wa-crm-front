export function parsePhoneNumber(phone: string) {
  const clean = (phone || '').trim();

  if (clean.startsWith('+')) {
    // Sort by code length descending to match longest code prefix first
    const sortedCountries = [...countries].sort((a, b) => b.code.length - a.code.length);
    for (const c of sortedCountries) {
      if (clean.startsWith(c.code)) {
        const local = clean.slice(c.code.length).replace(/\s+/g, '');
        return { countryCode: c.code, localNumber: local };
      }
    }

    // If not in list, find first space
    const spaceIndex = clean.indexOf(' ');
    if (spaceIndex > 0) {
      const code = clean.slice(0, spaceIndex);
      const local = clean.slice(spaceIndex + 1).replace(/\D/g, '');
      return { countryCode: code, localNumber: local };
    }
  }

  // Legacy or standard 10-digit number without "+"
  const digitsOnly = clean.replace(/\D/g, '');
  if (digitsOnly.length === 10) {
    return { countryCode: '+91', localNumber: digitsOnly };
  }
  if (digitsOnly.startsWith('91') && digitsOnly.length > 10) {
    return { countryCode: '+91', localNumber: digitsOnly.slice(2) };
  }

  return { countryCode: '+91', localNumber: digitsOnly };
}

export function cleanAndFormatPhone(rawPhone: string): string {
  const { countryCode, localNumber } = parsePhoneNumber(rawPhone);
  return localNumber ? `${countryCode} ${localNumber}` : countryCode;
}
